// Chuyen doi kieu du lieu cho bien

let number = 100; // number co kieu du lieu la integer
console.log("number = ", number);

number = "Hello"; // number co kieu du lieu la string
console.log("number = ", number);

// chuyen doi string thanh integer
number = parseInt("1234.56");
console.log('(int) number = ', number);

// chuyen doi string thanh float
number = parseFloat("1234.56");
console.log('(float) number = ', number);